    private void loadCoursesList() {
        DefaultListModel model = new DefaultListModel();
        for (int i = 0; i < system.getCourses().size(); i++) {
            model.addElement(system.getCourses().get(i).getTitle());
        }
        coursesList.setModel(model);
    }
